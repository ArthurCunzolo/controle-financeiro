    init() {
        this.currentLang = localStorage.getItem('fincontrol-lang') || 'en';
    },

    /** Get translation by key */
    get(key, params = {}) {
        const dict = this.translations[this.currentLang] || this.translations.en;
        let text = dict[key] || this.translations.en[key] || key;

        // Replace {param} placeholders
        Object.keys(params).forEach(p => {
            text = text.replace(new RegExp(`\\{${p}\\}`, 'g'), params[p]);
        });

        return text;
    },

    /** Set language and persist */
    setLang(lang) {
        this.currentLang = lang;
        localStorage.setItem('fincontrol-lang', lang);
    },

    /** Get the locale string for date formatting */
    getLocale() {
        return this.currentLang === 'pt' ? 'pt-BR' : 'en-US';
    },

    /** Get current language */
    getLang() {
        return this.currentLang;
    }
